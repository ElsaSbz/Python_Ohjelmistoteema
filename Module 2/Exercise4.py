# Write a program that asks the user for three integer numbers. The program prints out the sum, product, and average of the
# numbers.
first_number = int(input("Enter the first integer number: "))
second_number = int(input("Enter the second integer number: "))
third_number = int(input("Enter the third integer number: "))
print("The sum is", first_number+second_number+third_number)
print("The product is", first_number*second_number*third_number)
print ("The avarage is", (first_number+second_number+third_number)/3)